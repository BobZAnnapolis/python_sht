
print("\n08: IF-ELSE examples")
print("====================\n")

x = 13
y = 6
print("x=13; y=6\n")

print('\nprint x if it is less than y')
if x < y:
    print(x,'is less than',y)

print('\nprint x if it is greater than y ELSE print it is less than y')
if x > y:
    print(x,'is greater than',y)
else:
    print(x,'is less than',y)
