## for loops often used to iterate thru a list

print("\n06: FOR loops - often used to iterate thru a list\n")
print("=================================================\n")

exampleList = [1,6,7,3,6,9,0]

print("\n\tprint values in a supplied list")
for thing in exampleList:
    print(thing)

print("\n\tauto-generate a list of values using built-in Python 3.x range fcn")
for x in range(1,33):
    print(x)
